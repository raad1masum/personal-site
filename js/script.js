const times = [];
let fps;

function refreshLoop() {
  window.requestAnimationFrame(() => {
    const now = performance.now();
    while (times.length > 0 && times[0] <= now - 1000) {
      times.shift();
    }
    times.push(now);
    fps = times.length;
    n = document.getElementById("fps"),
    null != n ? n.innerHTML = this.fps.toFixed(1) + " fps" : void 0, 250
    console.log("FPS: " + fps);
    refreshLoop();
  });
}
